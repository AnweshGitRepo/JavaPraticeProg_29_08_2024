package javaProg;

public class ConvertASCIValueToString {
public static void main(String[] args) {	
	char c;
	for(int i=65;i<=90;i++) {	
		c=(char)i;
		System.out.println("Capital Alphabet  =>  "+i+"  =  "+c);
	}
	
for(int i=97;i<=122;i++) {
		
		c=(char)i;
		System.out.println("small Alphabet  =>  "+i+"  =  "+c);
	}
}
}
