package javaProg;

public class SwapingTwoNumberUsingThirdVarable {
public static void main(String[] args) {
	
	
	int x=20;
	int y=30;
	
	System.out.println("Before swaping  x = "+x+" ,  y = "+y);
	
	int z=x;
	    x=y;
	    y=z;
	    
	    System.out.println("After swaping  x = "+x+" ,  y = "+y);
}
}
